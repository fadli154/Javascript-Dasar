noAngkot = 1;
angkotBeroprasi = 6;
jumlahAngkot = 10;

while (noAngkot <= angkotBeroprasi) {
  console.log(`Angkot no. ` + noAngkot + ` Beroprasi dengan baik`);
  noAngkot++;
}

for (noAngkot; noAngkot <= jumlahAngkot; noAngkot++) {
  console.log(`Angkot no. ` + noAngkot + ` Sedang tidak Beroprasi`);
}
